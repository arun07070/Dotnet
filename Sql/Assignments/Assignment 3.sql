CREATE TABLE DEPT (
    deptno INT PRIMARY KEY,
    dname VARCHAR(50),
    loc VARCHAR(50)
);
CREATE TABLE EMP (
    empno INT PRIMARY KEY,
    ename VARCHAR(50),
    job VARCHAR(50),
    mgr INT NULL,
    hiredate DATE,
    sal DECIMAL(10,2),
    comm DECIMAL(10,2) NULL,
    deptno INT,
    FOREIGN KEY (deptno) REFERENCES DEPT(deptno)
);

